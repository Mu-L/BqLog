    /// <summary>
    /// Create a test_category_log object
    /// </summary>
    /// <param name="name">
    ///     If the test_category_log name is an empty string, bqLog will automatically assign you a unique name. 
    ///     If the test_category_log name already exists, it will return the previously existing test_category_log object and overwrite the previous configuration with the new config.
    /// </param>
    /// <param name="config">
    ///     test_category_log config string
    /// </param>
    /// <returns>
    ///     A test_category_log object, if create failed, the is_valid() method of it will return false
    /// </returns>
    public static new test_category_log create_log(string name, string config)
    {
        if (string.IsNullOrEmpty(config))
        {
            return new test_category_log();
        }
        ulong log_id = 0;
        unsafe
        {
            //char*[] is not supported in some IL2CPP old versions, so we have to use such an ugly way to do this.
            //char*[] category_names_array = new char*[categories_count];
            System.IntPtr names_array_alloc_ptr = Marshal.AllocHGlobal((int)(sizeof(byte*) * categories_count));
            byte** category_names_array_byte_ptr = (byte**)names_array_alloc_ptr.ToPointer();
            for (int i = 0; i < categories_count; ++i)
            {
                category_names_array_byte_ptr[i] = utf8_encoder.alloc_utf8_fixed_str(category_names[i]);
            }

            byte* utf8_name_bytes = utf8_encoder.alloc_utf8_fixed_str(name);
            byte* utf8_config_bytes = utf8_encoder.alloc_utf8_fixed_str(config);

            log_id = log_invoker.__api_create_log(utf8_name_bytes, utf8_config_bytes, categories_count, category_names_array_byte_ptr);

            for (int i = 0; i < categories_count; ++i)
            {
                utf8_encoder.release_utf8_fixed_str(category_names_array_byte_ptr[i]);
            }
            utf8_encoder.release_utf8_fixed_str(utf8_name_bytes);
            utf8_encoder.release_utf8_fixed_str(utf8_config_bytes);
            Marshal.FreeHGlobal(names_array_alloc_ptr);
        }
        log result = get_log_by_id(log_id);
        return new test_category_log(result);
    }


    /// <summary>
    /// Get a test_category_log object by it's name
    /// </summary>
    /// <param name="log_name">Name of the test_category_log object you want to find</param>
    /// <returns>A test_category_log object, if the test_category_log object with specific name was not found, the is_valid() method of it will return false</returns>
    public static new test_category_log get_log_by_name(string log_name)
    {
        test_category_log result = new test_category_log(log.get_log_by_name(log_name));
        if(!result.is_valid())
        {
            return result;
        }
        //check categories
        if (result.get_categories_count() != test_category_log.categories_count)
        {
            return new test_category_log();
        }
        for (uint i = 0; i < result.get_categories_count(); ++i)
        {
            if (!test_category_log.category_names[(int)i].Equals(result.get_categories_name_array()[(int)i]))
            {
                return new test_category_log();
            }
        }
        return result;
    }


